#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*s;
	int		i;
	int		j;

	i = 0;
	while (src[i])
		i++;
	s = malloc(sizeof(char) * (i + 1));
	if (!s)
		return (0);
	j = 0;
	while (src[j])
	{
		s[j] = src[j];
		j++;
	}
	s[j] = '\0';
	return (s);
}
