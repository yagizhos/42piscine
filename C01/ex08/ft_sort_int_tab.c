void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	n;

	j = 0;
	while (j < size - 1)
	{
		i = 0;
		while (i < size - j - 1)
		{
			if (tab[i] > tab[i + 1])
			{	
				n = tab[i];
				tab[i] = tab[i + 1];
				tab[i + 1] = n;
			}
			i++;
		}
		j++;
	}
}
